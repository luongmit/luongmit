public class MyDate {
    private int day;
    private int month;
    private int year;

    public MyDate(int day, int month, int year) {
        this.day = day;
        this.month = month;
        this.year = year;
    }

    public void display() {
        // Định dạng dd/mm/yyyy
        System.out.printf("%02d/%02d/%04d%n", day, month, year);
    }
}
